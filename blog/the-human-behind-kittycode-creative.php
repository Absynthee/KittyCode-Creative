                    <p>Hello! I'm Austin—definitely not a cat, but a real human! I'm an experienced digital designer and
                        front-end web developer with nearly 10 years in the tech and design industries. As the owner and
                        sole developer here at KittyCode Creative, I’m dedicated to delivering high-quality,
                        personalised web solutions. If you’re interested in connecting with me directly <a
                            class="blog-link" target="_blank" href="https://www.linkedin.com/in/aspillman/">you can find
                            me on LinkedIn</a>.</p>
                    <p>Until recently, I was part of the team at House337, where I designed and developed web pages for
                        Apple's EU and CEMEA sites. This experience greatly boosted my confidence and honed my ability
                        to create high-standard websites. Even before this role, I had a lifelong passion for design. I
                        studied digital design and web development at Anglia Ruskin University, where I also explored 3D
                        modelling, 2D & 3D video production, and audio design. My first role after university was as a
                        volunteer web designer for Young People Littleport (YPL). </p>
                    <p>I started KittyCode Creative to offer affordable, high-quality web services to small businesses
                        and individuals who want to enhance their online presence without breaking the bank or
                        navigating the confusing "do it yourself" web builder landscape. Thanks to my experience and low
                        overheads as a sole developer, I can provide unique, personalised services at competitive
                        prices.</p>
                    <p>My approach is straightforward—keeping web design and development simple. This field can be
                        complicated and overwhelming, even for seasoned professionals. With so many platforms,
                        languages, libraries, and third-party services available, it’s easy to get lost. My goal is to
                        cut through the noise and deliver clean, hand-crafted HTML, CSS, and JavaScript as the
                        foundation of my websites. In cases where clients need a robust blog or ready-built Content
                        Management System (CMS), I use Webflow to ensure the best possible experience.</p>
                    <p>I’m passionate about making the web a better, more inclusive place. My commitment is to create
                        websites that are accessible to everyone, regardless of conditions, disabilities, or devices.
                        This reflects the way I live my life—ensuring that everyone feels included and has a sense of
                        belonging. Accessibility is a core part of KittyCode Creative’s mission.</p>
                    <p class="blog-quote">Making the web accessible benefits individuals, businesses, and society. Your
                        website will be fully perceivable, operable, understandable and robust.</p>
                    <p class="blog-quote-after">Modern web standards have evolved significantly in recent
                        years—technologies have improved, languages have become more user-friendly, and web builders
                        have gained popularity. Staying up to date with these trends is crucial for providing clients
                        with the best possible experience. This commitment to continuous improvement aligns with
                        KittyCode Creative’s vision for the future. I’ll be writing a post next week about modern CSS,
                        so stay tuned!</p>
                    <p>You might be wondering—<em>why KittyCode?</em> I chose the name KittyCode Creative partly because
                        of my love for cats, but also because it’s playful and approachable. I wanted to stand out from
                        the more corporate, professional names some businesses use. The word "Creative" highlights that
                        we don’t just focus on development—design is a major part of what we do. I wanted to blend
                        "Code" (which implies hand-crafted coding) and "Creative" together. The idea of cats tapping
                        away at a keyboard is something I love, and thus, KittyCode Creative and its mascot, Code, were
                        born.</p>
